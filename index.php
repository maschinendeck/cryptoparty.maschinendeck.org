    <div class="row">
      <h1>Über Cryptopartys</h1>
      <div class="block"></div>
      <p>Cryptoparty? Was ist das überhaupt?</p>
      <p>Bei einer Cryptoparty wird versucht, den Teilnehmern die Grundkenntnisse und Prinzipien der Verschlüsselung näher zu bringen, um somit immer mehr Menschen mit der sicheren Kommunikation im Netz vertraut zu machen. Dabei wird auf den Einsatz von freier und kostenloser Open-Source-Software sehr großen Wert gelegt.
        <br>Dank den Enthüllungen von Edward Snowden ist nun klar, dass Geheimdienste wie NSA und BND die Bürger im Internet ausspionieren. Diese Spionage können wir zwar nicht verhindern, jedoch können wir Techniken einsetzen, die es den Geheimdiensten wesentlich schwieriger macht unsere Daten auszuwerten.
        Wir stellen in Cryptopartys Programme, Tools und Hinweise vor, welche ohne großen Aufwand das Surfen im Internet sowie das versenden von E-Mails verschlüsselt.
      </p>
    </div>
    <div class="row">
      <div class="six columns">
        <h3><span class="typcn typcn-key-outline icon"></span>Verschlüsselung von E-Mails</h3>
        <p>Lernen, wie einfach die Verschlüsselung von E-Mails ist. Ohne großen Aufwand lassen sich E-Mails sicher Verschlüsseln, sodass keine Dritten den Inhalt dieser Mails auslesen können.</p>
      </div>
      <div class="six columns">
        <h3><span class="typcn typcn-lock-closed-outline icon"></span>Anonymes surfen im Internet</h3>
        <p>Mit Hilfe eines speziellen Browsers, dem TOR-Browser, anonym und sicher im Internet surfen. Im Laufe der Veranstaltung wird die besondere Funktionsweise von TOR und die Unterschiede zum "normalen" Internet vorgestellt.</p>
      </div>
      <div class="row">
        <div class="six columns">
          <h3><span class="typcn typcn-messages icon"></span>Sicheres Instant-Messaging</h3>
          <p>Der weltweit am meisten genutzte Messager ist WhatsApp. Seit dem Kauf 2014 von Facebook und dem Datenaustausch zwischen Facebook und WhatsApp ist die Suche nach einem sicheren Messager immer größer geworden. In der Cryptoparty versuchen wir, sichere und freie Messanger vorzustellen sowie Argumente zum Überreden von Freunden und Familie bereit zu stellen.</p>
        </div>
        <div class="six columns">
          <h3><span class="typcn typcn-image-outline icon"></span>Sicheres Aufbewahren der Urlaubsfotos</h3>
          <p>Nichts ist peinlicher als den USB-Stick mit den Urlaubsfotos zu verlieren. Der Finder des USB-Sticks kann ohne Schwierigkeiten den kompletten Inhalt des Sticks auslesen und alle private Urlaubsfotos ansehen. Mit Hilfe von Festplattenverschlüsselung währe das nicht passiert. Darum wird in der Cryptoparty vorgestellt, USB-Sticks oder auch ganze Computerfestplatten sicher zu verschlüsseln, sodass Ein Zugriff ohne Passwort nicht möglich ist.</p>
        </div>
      </div>
